package ug.lab.zad1;

public class Kalkulator {
	
	public int dodaj(int a, int b) {
		return a + b;
	}

	public int odejm(int a, int b) {
		return a - b;
	}

	public int mnoz(int a, int b) {
		return a * b;
	}

	public int dziel(int a, int b) {
		return a / b;
	}
	
	public boolean wieksze(int a, int b) {
		return a > b;
	}

}
