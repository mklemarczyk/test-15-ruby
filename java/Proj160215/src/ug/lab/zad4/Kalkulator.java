package ug.lab.zad4;

public class Kalkulator {
	
	public double dodaj(double a, double b) {
		return a + b;
	}

	public double odejm(double a, double b) {
		return a - b;
	}

	public double mnoz(double a, double b) {
		return a * b;
	}

	public double dziel(double a, double b) {
		return a / b;
	}
	
	public boolean wieksze(double a, double b) {
		return a > b;
	}

}
