
public class Person {
	
	private String NAME;
	private String LAST_NAME;
	private int AGE;

	public String getNAME() {
		return NAME;
	}

	public void setNAME(String nAME) {
		NAME = nAME;
	}

	public String getLAST_NAME() {
		return LAST_NAME;
	}

	public void setLAST_NAME(String lAST_NAME) {
		LAST_NAME = lAST_NAME;
	}

	public int getAGE() {
		return AGE;
	}

	public void setAGE(int i) {
		AGE = i;
	}

}
